package BestAlbums;

public class InnerLoop {

    public static void main(String[] args) {

        for (int i=0;i<4;i++) {
            System.out.println("Election");
            for (int p=0;p<3;p++) {
                System.out.println("Biden");
                for (int a=0;a<2;a++) {
                    System.out.println("Trump");
                    System.out.println("Kamala");

                }}
            }

    }
}
